async def seed_register_maps(db):
    """
    Seed predefined register maps into the database.
    Skips maps that already exist (by name).
    """
    from app.models.integration import RegisterMap
    from sqlalchemy import select

    for seed in REGISTER_MAP_SEEDS:
        result = await db.execute(
            select(RegisterMap).where(RegisterMap.name == seed["name"])
        )
        existing = result.scalar_one_or_none()
        if existing:
            continue

        reg_map = RegisterMap(
            name=seed["name"],
            protocol=seed["protocol"],
            manufacturer=seed["manufacturer"],
            model=seed.get("model"),
            description=seed.get("description"),
            version=seed.get("version", "1.0"),
            registers=seed["registers"],
        )
        db.add(reg_map)

    await db.commit()


