async def seed_device_profiles(db):
    """Seed built-in device profiles for BACnet and cloud integrations."""
    from app.models.device_profile import DeviceProfile
    from sqlalchemy import select

    for seed in DEVICE_PROFILE_SEEDS:
        result = await db.execute(
            select(DeviceProfile).where(
                DeviceProfile.manufacturer == seed["manufacturer"],
                DeviceProfile.model == seed["model"],
            )
        )
        if result.scalar_one_or_none():
            continue

        profile = DeviceProfile(
            manufacturer=seed["manufacturer"],
            model=seed["model"],
            display_name=seed["display_name"],
            description=seed.get("description"),
            equipment_type=seed.get("equipment_type", "compressor"),
            protocol=seed.get("protocol", "modbus_tcp"),
            default_port=seed.get("default_port", 502),
            register_map=seed.get("register_map", {}),
            bacnet_config=seed.get("bacnet_config"),
            is_builtin=True,
        )
        db.add(profile)

    await db.commit()
